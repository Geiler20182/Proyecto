package Usuario

class Piloto {
    // Atributos
    var _nombre : String = _
    var _genero : String = _

    // Getters
    def nombre = _nombre
    def genero = _genero

    // Setters
    def nombre_= (nuevoNombre : String) = _nombre = nuevoNombre
    def genero_= (nuevoGenero : String) = _genero = nuevoGenero
}